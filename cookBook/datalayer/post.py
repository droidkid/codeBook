class Post:

    def set_title(self, title):
        self.title = title

    def get_title(self):
        return self.title

    def set_content(self, content):
        self.content = content

    def get_content(self):
        return self.content

    def set_code(self, code):
        self.code = code

    def get_code(self):
        return self.code
